# Kaggleの概要

# ・Kaggleとは？
#  ・企業や研究機関が提供するデータ分析の実践的な課題に対して、
#    世界中の参加者が制度を競うプラットフォーム
#  ・データ収集や環境構築の手間がかからない
#  ・優秀なスコアを残した参加者には、賞金やスコアに応じた称号が与えられる
#  ・多くの企業が、Kaggleのスコアをリクルーティングに利用している

# ・Kaggleのメリット
#  ・生のビッグデータが用意されている
#   ・50万件のクレジットカード等の決済履歴
#   ・6000万件の米小売大手の売上履歴、などなど
#  ・スキルの証明
#   ・機械学習エンジニア、データサイエンティストの求人要件によく掲載されている
#  ・楽しくて実力がつく
#   ・自分の順位がリアルタイムに表示され、実績に応じた称号が与えられる

# ・Kaggleの人気
#  ・Googleトレンド「Kaggle」
#     https://trends.google.co.jp/trends/explore?date=today%205-y&q=Kaggle&hl=ja

